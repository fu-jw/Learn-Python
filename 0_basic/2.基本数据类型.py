# 整型
x = 20
y = -10
print(x + y)
#################################
# 浮点型
a = 3.14
b = -1.01
print(a - b)
#################################
# 布尔型
c = 2 > 1
d = 2 < 1
e = 2 != 1
print(c)  # True
print(d)  # False
print(e)  # True

# 内置函数bool(),返回该变量的布尔值
print(bool(d))  # False
# 注意：只有零为False，非零为True
print(bool(0))  # False
print(bool(1))  # True
print(bool(-1))  # True
print(bool("0"))  # True
